      <h2 id="informacion">Información</h2>
      <p>
        H.O.T. (에이치오티; acrónimo de Highfive Of Teenagers) fue un grupo de chicos de cinco miembros de SM Entertainment.
        Debutaron oficialmente el 7 de septiembre de 1996 con su primer álbum, We Hate All Kinds of Violence. Se
        disolvieron en mayo de 2001. <br><br>
        El 15 de febrero de 2018, realizaron un concierto de reunión para el programa de variedades Infinite Challenge.
        <br><br>
        Se les considera el primer grupo ídolo del K-pop y su fórmula exitosa se convirtió en el modelo para muchos
        grupos de K-pop que los siguieron.
      </p>
