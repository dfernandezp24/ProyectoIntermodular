    <section>
      <h2 id="historia">Historia</h2>

      <h3 id="pre-debut">Pre-Debut</h3>
      <p>
        H.O.T. fue formado por el productor Lee Soo Man, el CEO de SM Entertainment, en 1996. Su música comprende una
        mezcla de rap, baladas, danza, y rock tardío. El cantante Kang Ta fue quien contribuyó mayormente en las
        baladas. <br><br>
        Lee Soo Man confirmó como primer integrante del grupo a Kang Ta. Él lo vio bailando y cantando con un amigo en
        un parque de diversiones y quedó impresionado. <br><br>
        El segundo integrante en ser confirmado fue Hee Jun, quien después de una audición fue añadido a la banda. <br>
        Hee Jun invitó a su amigo Jae Won a la audición, que fue posteriormente confirmado como el tercer integrante.
        <br>
        El cuarto integrante en unirse al grupo fue Woo Hyuk, después de ganar el primer lugar en un concurso de baile.
        <br><br>
        SM Entertainment después viajó a Los Ángeles para llevar a cabo audiciones. Tony An fue descubierto
        originalmente por Brothers Entertainment en los Estados Unidos. Él y su amigo Andy Lee se unieron al proyecto;
        sin embargo, los padres de Lee después retiraron su apoyo, lo que hizo que lo abandonara, quedando solo Tony An
        como el quinto integrante.
      </p>

      <h3 id="debut">1996: Debut con su Primer Álbum 'We Hate All kind of Violence...'</h3>
      <p>
        H.O.T lanzo su primer álbum debut 'We Hate All kind of Violence...' lanzado el 7 de Septiembre de 1996. 'Warrior
        Descendants' fue la canción principal de su álbum debut, contenía un mensaje para los adolescentes de aquella
        época, por lo que fue uno de los factores más importantes que contribuyeron en que H.O.T fuera considerado
        ídolos entre los jóvenes.
        El mensaje de la canción es la lucha contra la violencia y la lucha contra la severidad en las escuelas. Así
        H.O.T. sienta las bases de su historia con esta canción, ya que en sus álbumes posteriores se referirían a
        muchas cuestiones de la sociedad coreana, especialmente lo relacionado con la generación más joven.
        El grupo regreso a las promociones con el sencillo 'Candy', siendo este su primer gran éxito, convirtiendose en
        un verdadero himno de la época.
      </p>

      <h3 id="1997">1997: Segundo Álbum 'Wolf And Sheep'</h3>
      <p>
        H.O.T lanzo en junio de 1997 su segundo álbum titulado 'Wolf And Sheep', este generó controversia, ya que la
        canción que lleva el mismo título del álbum utiliza un lenguaje considerado ofensivo lo que provocó su
        prohibición en las principales estaciones de radio. <br><br>
        El grupo regreso a las promociones con el sencillo 'We are the Future' al tratar el tema de la presión que los
        adultos ponen sobre los jóvenes diciéndoles cómo vivir sus vidas, generó críticas por parte de los adultos y
        muchos padres prohibieron que sus hijos escucharan su música. Sin embargo, la canción consiguió de todas maneras
        convertirse en número 1.
      </p>

      <h3 id="1998">1998: Tercer Álbum 'Resurrection'</h3>
      <p>
        En septiembre 1998, H.O.T. lanza su tercer álbum 'Resurrection'. Este álbum contó con un estilo más "serio" y
        rockero.
      </p>

      <h3 id="1999">1999: Cuarto Álbum 'I Yah!'</h3>
      <p>
        En 1999 lanzan su cuarto álbum 'I Yah!' del cual su tema principal (I Yah!) enfatizaba la conciencia social.
        <br><br>
        La canción está inspirada en un accidente ocurrido en 1999 en el que 23 niños murieron a causa de un incendio.
        Parte del coro dice “Quién les dio permiso de apagar la llama de los niños que ni siquiera podían ser quemados”,
        que sin duda reflejan el sentimiento hacia la tragedia.
      </p>

      <h3 id="2000">2000: Quinto Álbum 'Outside Castle'</h3>
      <p>
        H.O.T. lanza su quinto y último álbum titulado 'Outside Castle' el 29 de septiembre del 2000. Todos los miembros
        participaron en la composición de las letras. <br><br>
        H.O.T. lanzó su última canción promocional de su quinto álbum titulada 'A Song for Lady', que fue utilizado en
        el drama coreano 'Otoño en mi corazón' (Autumn in my Heart).
      </p>


      <h3 id="disolucion">2001: Disolución</h3>
      <p>
        A principios del 2001, a medida que se acercaba la fecha de término de sus contratos con SM Entertainment, H.O.T
        se vio rodeado de rumores de separación. <br><br>
        En mayo del 2001, Woo Hyuk, Tony An y Jae Won anunciaron en una conferencia de prensa que dejaban el grupo
        debido a desacuerdos con la agencia respecto a sus nuevos contratos. Los tres anunciaron que les hubiese gustado
        seguir activos con el grupo pero debido a las circunstancias se les hacía difícil hacerlo. <br><br>
        Cada miembro recibió regalías equivalentes a 1 centavo por cada copia de álbumes que vendieron, lo que
        significaba que cada uno de ellos ganaría cerca de $10.000 dolares por la venta de álbumes. <br><br>
        Después de la separación, SM Entertainment ofreció contratos atractivos a Kang Ta y Hee Jun como solistas.
        Mientras que Woo Hyuk, Tony An y Jae Won formaron un nuevo grupo llamado J.T.L (las iniciales de cada uno de
        ellos) bajo otra agencia.
      </p>
    </section>

