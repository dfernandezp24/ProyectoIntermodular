      <h2 id="historia">Historia</h2>

      <h3 id="pre-debut">Pre-Debut</h3>
      <p>
        El 10 de febrero de 2025, Starship Entertainment abrió las cuentas en redes sociales de su entonces futuro grupo femenino, KiiiKiii. Se encargó a unos 20 artistas y creadores de contenido que presentaran el grupo mediante una serie de publicaciones en Instagram. Además, una foto en una de las publicaciones de Instagram insinuaba una alineación de cinco integrantes, ya que incluía a Kya, Subin, Sumin, Jiyu y Haum.
      </p>

      <h3 id="2025">2025: Debut con Uncut Gem, primer win en un music show, nombre de club de fans, "Dancing Alone", "Dancing Alone (PARKMOONCHI Mix).zip", "To Me From Me (Prod. Tablo)"</h3>
      <p>
        El 12 de febrero de 2025, se anunció que KiiiKiii debutaría en marzo. El 16 de febrero, el grupo anunció el lanzamiento de su EP debut, Uncut Gem, el 24 de marzo. Ese mismo día, se anunció oficialmente la identidad de los miembros: Leesol, Sui, Jiyu, Haum y Kya, y se lanzó el video musical de "I Do Me". El 24 de febrero, se lanzó oficialmente como sencillo en plataformas de streaming. <br><br>
        El 5 de abril, KiiiKiii logró su primera win en un music show en Show! Music Core con "I Do Me". <br><br>
        El 24 de abril, el grupo anunció el nombre de su fandom: "TiiiKiii". Inspirado en la palabra "Tiki-Taka", el nombre refleja la esperanza de que KiiiKiii y los fans siempre estén en armonía, como la pareja perfecta, y se conviertan en verdaderos amigos que compartan risas y alegría. <br><br>
        El 17 de julio, se anunció que KiiiKiii se prepara para un nuevo lanzamiento con el objetivo de regresar en agosto. El 26 de julio, se anunció que el grupo lanzaría su segundo sencillo digital, "Dancing Alone", el 6 de agosto. <br><br>
        El 28 de octubre de 2025, se reveló un video teaser de su próxima canción "To Me From Me", programada para lanzarse el 4 de noviembre, como banda sonora de la novela web colaborativa del grupo Dear.X: Tomorrow's Me to Today's Me, que se estrena el mismo día.
      </p>

      <h3 id="2026">2026: Delulu Pack</h3>
      <p>
        El 31 de diciembre de 2025, KiiiKiii publicó nuevas imágenes teaser con el texto "En la quietud, comienza un delulu". Además, se modificó el diseño de su sitio web oficial, que ahora incluye el título "Delulu World". Posteriormente, el 1 de enero de 2026, Starship Entertainment confirmó que KiiiKiii regresaría a finales de enero. El 5 de enero, el grupo KiiiKiii anunció el lanzamiento de su segundo EP, Delulu Pack, el 26 de enero de 2026.
      </p>

      
